package com.bs.outer.entity;


public class HotCoolBean{
	//冷热针对的号码
	private int number;

	private String threeFollow;

	private String threeNoFollow;

	private String follow;

	private String noFollow;

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}

	public String getThreeFollow() {
		return threeFollow;
	}

	public void setThreeFollow(String threeFollow) {
		this.threeFollow = threeFollow;
	}

	public String getThreeNoFollow() {
		return threeNoFollow;
	}

	public void setThreeNoFollow(String threeNoFollow) {
		this.threeNoFollow = threeNoFollow;
	}

	public String getFollow() {
		return follow;
	}

	public void setFollow(String follow) {
		this.follow = follow;
	}

	public String getNoFollow() {
		return noFollow;
	}

	public void setNoFollow(String noFollow) {
		this.noFollow = noFollow;
	}
}